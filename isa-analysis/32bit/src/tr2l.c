#include <stdio.h>
#include <stdint.h>

uint32_t tr2l (uint32_t rs1, uint32_t rs2) {
	uint32_t rd; 	
	rd = ((rs1 & 0x00008000) << 16) | ((rs2 & 0x00008000) << 15) | ((rs1 & 0x00004000) << 15) | ((rs2 & 0x00004000) << 14) |
		((rs1 & 0x00002000) << 14) | ((rs2 & 0x00002000) << 13) | ((rs1 & 0x00001000) << 13) | ((rs2 & 0x00001000) << 12) |
		((rs1 & 0x00000800) << 12) | ((rs2 & 0x00000800) << 11) | ((rs1 & 0x00000400) << 11) | ((rs2 & 0x00000400) << 10) |
		((rs1 & 0x00000200) << 10) | ((rs2 & 0x00000200) << 9) | ((rs1 & 0x00000100) << 9) | ((rs2 & 0x00000100) << 8) |
		((rs1 & 0x00000080) << 8) | ((rs2 & 0x00000080) << 7) | ((rs1 & 0x00000040) << 7) | ((rs2 & 0x00000040) << 6) |
		((rs1 & 0x00000020) << 6) | ((rs2 & 0x00000020) << 5) | ((rs1 & 0x00000010) << 5) | ((rs2 & 0x00000010) << 4) |
		((rs1 & 0x00000008) << 4) | ((rs2 & 0x00000008) << 3) | ((rs1 & 0x00000004) << 3) | ((rs2 & 0x00000004) << 2) |
		((rs1 & 0x00000002) << 2) | ((rs2 & 0x00000002) << 1) | ((rs1 & 0x00000001) << 1) | ((rs2 & 0x00000001) ) ;
	return rd;
}

int main (int argc, char *argv[]) {
	
	uint32_t rs1 = 0xffffffff; 		
	uint32_t rs2 = 0;
	uint32_t rd;

	rd = tr2l (rs1, rs2);

	printf("result: %08x\n", rd);
	
	return 0;
}
